class JavaExecutionFlow {

    /*
     * JDK (Java Development Kit):
     * - Used by developers to write and compile Java programs
     * - Contains compiler (javac), JRE, and development tools
     *
     * JRE (Java Runtime Environment):
     * - Required to run Java applications
     * - Contains JVM and core libraries
     *
     * JVM (Java Virtual Machine):
     * - Executes Java bytecode
     * - Provides platform independence
     *
     * JAVA EXECUTION FLOW:
     *
     * 1. Developer writes Java source code (.java file)
     * 2. javac compiler compiles source code into bytecode (.class file)
     * 3. JVM loads the .class file using Class Loader
     * 4. Bytecode Verifier checks security and correctness
     * 5. Execution Engine converts bytecode to machine code
     * 6. JVM executes the program line by line
     *
     * KEY POINT:
     * - Java is platform independent because bytecode runs on JVM
     */
}